package tetromino;

public class TetrominoColor {
    /**
     * ANSI color code for each tetromino
     */
    public static final String ITETROMINO_COLOR = "\\u001B[31m"; // red
    public static final String JTETROMINO_COLOR = "\\u001B[32m"; // green
    public static final String LTETROMINO_COLOR = "\\u001B[33m"; // yellow
    public static final String OTETROMINO_COLOR = "\\u001B[34m"; // blue
    public static final String STETROMINO_COLOR = "\\u001B[35m"; // purple
    public static final String TTETROMINO_COLOR = "\\u001B[36m"; // cyan
    public static final String ZTETROMINO_COLOR = "\\u001B[37m"; // white

    public static final String ANSI_RESET = "\u001B[0m";
}
